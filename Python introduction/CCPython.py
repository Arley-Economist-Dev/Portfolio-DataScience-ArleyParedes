for i in [1,2,3,4,5]:
    print(i)                #primera linea en el bloque for i
    for j in [1,2,3,4,5]:   
        print(j)            #primera linea en el bloque for j
        print(i+j)          #la ultima linea en el bloque for j
    print(i)                #ultima linea en el bloque for i
    print("done looping")






